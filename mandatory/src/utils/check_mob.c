#include "cub3d.h"

short	check_mob(t_map *map)
{
	size_t	y;
	size_t	x;

	y = 0;
	x = 0;
	while (y < map->height)
	{
		while (map->grid[y][x])
		{
			if (map->grid[y][x] == '4')
				return (1);
			x++;
		}
		y++;
	}
	return (0);
}
